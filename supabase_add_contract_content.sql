-- ============================================
-- Добавление поля content в таблицу contracts
-- для сохранения отредактированного HTML контента
-- ============================================

-- Добавляем поле content (отредактированный контент договора)
ALTER TABLE contracts 
ADD COLUMN IF NOT EXISTS content TEXT;

-- Добавляем комментарий к полю
COMMENT ON COLUMN contracts.content IS 'Отредактированный HTML контент договора (если был изменен вручную)';

-- ============================================
-- Проверка
-- ============================================
SELECT 'Поле content добавлено в таблицу contracts!' as status;
